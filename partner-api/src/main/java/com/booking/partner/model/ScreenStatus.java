package com.booking.partner.model;

/**
 * Screen Status Enumeration
 *
 * Defines operational status of a screen
 *
 * @author XYZ Booking Platform
 */
public enum ScreenStatus {
    /**
     * Screen is operational
     */
    ACTIVE,

    /**
     * Screen is temporarily unavailable
     */
    INACTIVE,

    /**
     * Screen under maintenance
     */
    UNDER_MAINTENANCE
}
