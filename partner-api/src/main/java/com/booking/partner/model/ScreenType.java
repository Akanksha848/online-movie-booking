package com.booking.partner.model;

/**
 * Screen Type Enumeration
 *
 * Defines different types of screens/halls available
 *
 * @author XYZ Booking Platform
 */
public enum ScreenType {
    /**
     * Regular 2D screen
     */
    REGULAR,

    /**
     * 3D enabled screen
     */
    THREE_D,

    /**
     * IMAX large format screen
     */
    IMAX,

    /**
     * 4DX with motion seats and effects
     */
    FOUR_DX,

    /**
     * Dolby Atmos audio system
     */
    DOLBY_ATMOS,

    /**
     * Premium/VIP screen
     */
    PREMIUM
}
